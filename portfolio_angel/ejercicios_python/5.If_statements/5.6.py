# In-elif-else chain / Persons stage of life
age = 53

if age < 2:
    print("U are just a baby")
elif 2 <= age < 4:
    print("U are a toddler")
elif 4 <= age < 13:
    print("U are a kid")
elif 13 <= age <= 20:
    print("U are a teenager")
elif 20 <= age <= 65:
    print("U are a adult")
else:
    print("U are an elder")